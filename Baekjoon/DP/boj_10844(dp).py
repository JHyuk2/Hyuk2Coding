## 계단수.

# dp[1] = [1~9] = 9x1
# dp[2] = [10, 12, 21, 23, 32, 34, ... 87,89, 98] = 8x2 +1 = 17 = (9-1) x 2 + 1
# dp[3] = [101, 121, 123, 212, 210,  ..., 789,..., 898, 989, 987] = (17-2)x2 + 2 = 32
# dp[4] = [1010, 1012, 1210, 1212,...,3210, ...,  8987, 8989, ..., 9898] = (32-3)x2 + 3 = 61 
# dp[5] = (61-6)*2 + 6 = 55x2 + 6 = 116
# dp[6] = (116-10)x2 + 10 = 212 + 10 = 222

# 점화식으로 푸는 방법도 있지만, bfs로 풀어보고싶은 이 마음...

N = int(input())
step = [0, 1, 2, 3]
dp = [0 for _ in range(100+1)]

dp[1] = 9
if N > 1:
    for i in range(2, N+1):
        if i < 5:
            dp[i] = (dp[i-1]-step[i-1])*2 + step[i-1]
        else:
            temp = (i-1)*(i-2)//2
            dp[i] = (dp[i-1]-temp)*2 + temp

print(dp[i]%1000000000)
